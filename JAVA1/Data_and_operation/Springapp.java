
public class Springapp {
   public static void main(String[] args) {
	 System.out.println("hello world");
	 System.out.println("hello"
	 +" world");
	 
	
	 System.out.println("hello \nworld");   // \nは改行（New line)。改行したい文字の前に作成する。
	 
	 System.out.println("hello \"world\""); // \はエスケープ(Escape)。記号が持っている文法的な意味を無視する。
	 
	 System.out.println("振"
			 +"り向いた姿。\nまっすぐな瞳で、\n後悔のない声で \n\"シロウー\"\n\"貴方を愛してます。\"");
	 
}
}
